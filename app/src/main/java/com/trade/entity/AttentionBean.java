public class AttentionBean {

    private String attentionId;

    private String attentionNum;

    private String attentionTitle;

    private String attentionDetail;

    private String attentionTime;

    public String getAttentionId() {
        return attentionId;
    }

    public void setAttentionId(String attentionId) {
        this.attentionId = attentionId;
    }

    public String getAttentionNum() {
        return attentionNum;
    }

    public void setAttentionNum(String attentionNum) {
        this.attentionNum = attentionNum;
    }

    public String getAttentionTitle() {
        return attentionTitle;
    }

    public void setAttentionTitle(String attentionTitle) {
        this.attentionTitle = attentionTitle;
    }

    public String getAttentionDetail() {
        return attentionDetail;
    }

    public void setAttentionDetail(String attentionDetail) {
        this.attentionDetail = attentionDetail;
    }

    public String getAttentionTime() {
        return attentionTime;
    }

    public void setAttentionTime(String attentionTime) {
        this.attentionTime = attentionTime;
    }
}
